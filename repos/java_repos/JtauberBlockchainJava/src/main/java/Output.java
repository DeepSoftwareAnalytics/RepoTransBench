import java.util.List;

public class Output {
    private long value;
    private List<String> script;

    // Getters and Setters
    public long getValue() {
        return value;
    }

    public void setValue(long value) {
        this.value = value;
    }

    public List<String> getScript() {
        return script;
    }

    public void setScript(List<String> script) {
        this.script = script;
    }
}
